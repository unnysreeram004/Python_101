# This code snipet helps explain the concept of int and float data types in Python.

# An integer (int) is a whole number without a decimal point.
whole_number = 10
print("Integer value:", whole_number)
print("Type of whole_number:", type(whole_number))

# A float is a number that has a decimal point.    
decimal_number = 10.5
print("Float value:", decimal_number)
print("Type of decimal_number:", type(decimal_number))
